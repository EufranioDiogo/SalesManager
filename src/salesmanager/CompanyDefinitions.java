    private void setCompanyInformation() {
        try {
            String query = "SELECT * FROM company;";
            PreparedStatement statement = connection.prepareStatement(query);

            ResultSet result = statement.executeQuery();
            if (result.next()) {
                companyName = result.getString(1);
                this.companyNameTextBox.setText(companyName);
                this.nifTextBox.setText(result.getString(2));
                this.localizacaoTextBox.setText(result.getString(3));
                this.ownerTextBox.setText(result.getString(4));
            }
        } catch (Exception e) {
            this.companyName = "k";
        }
        
    }
