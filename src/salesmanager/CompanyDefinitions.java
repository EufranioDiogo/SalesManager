    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String newCompanyName = this.companyNameTextBox.getText().trim();
        
        if (newCompanyName.length() > 0) {
            String newNIF = this.nifTextBox.getText().trim();
            String newlocalizacao = this.localizacaoTextBox.getText().trim();
            String newOwner = this.ownerTextBox.getText().trim();
            
            try {
                String query = "UPDATE company SET name=?, nif=?, localizacao=?, proprietario=? WHERE name=?";
                PreparedStatement statement = connection.prepareStatement(query);
                
                statement.setString(1, newCompanyName);
                statement.setString(2, newNIF);
                statement.setString(3, newlocalizacao);
                statement.setString(4, newOwner);
                statement.setString(5, companyName);
                
                statement.executeUpdate();
                companyName = newCompanyName;
                JOptionPane.showMessageDialog(null, "Dados actualizados;");
            } catch (Exception e) {
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

